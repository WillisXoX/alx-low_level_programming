#ifndef MAIN_H
#define MAIN_H

/**
* _putchar - puts character to standard output
* @c: character to put to standard output
*/

int _putchar(char c);

/**
 * _isupper - checks for uppercase character
 * @c: character to put to standard output
 */

int _isupper(int c);

/**
 * _isdigit - checks for a digit (0 through 9)
 * @c - integer variable
 */

int _isdigit(int c);

/**
 * mul - multiplies two integers
 * @a - integer variable
 * @b - integer variable
 */

int mul(int a, int b);

#endif
